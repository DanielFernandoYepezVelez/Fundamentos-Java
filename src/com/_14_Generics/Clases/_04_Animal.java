package com._14_Generics.Clases;

public class _04_Animal {

    private String nombre;
    private String tipo;

    public _04_Animal(String nombre, String tipo) {
        this.nombre = nombre;
        this.tipo = tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public String getTipo() {
        return tipo;
    }

}